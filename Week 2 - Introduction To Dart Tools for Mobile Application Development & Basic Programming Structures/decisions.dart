void main() {
  // If statement
  var num = 5;
  if (num > 0) {
    print("The value '$num' is positive.");
  }
  print('');

  // If Else statement
  var a = 12;
  if (a % 2 == 0) {
    print("The value '$a' is even.");
  } else {
    print("The value '$a' is odd.");
  }
  print("");

  // Else if Statements
  var x = 2;
  if (x > 0) {
    print("$x is positive.");
  }
  else if (x < 0) {
    print("$x is negative.");
  } else {
    print("$x is neither positive nor negative.");
  }
  print("");

}
